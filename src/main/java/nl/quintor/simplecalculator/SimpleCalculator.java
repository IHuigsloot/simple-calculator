package nl.quintor.simplecalculator;

public class SimpleCalculator {

    public double add(int a, int b) {
        return a + b;
    }

    public double subtract(int a, int b) {
        return a - b;
    }

    public double multiple(int a, int b) {
        return a * b;
    }

    public double divide(int a, int b) {
        return a / (double) b;
    }
}
